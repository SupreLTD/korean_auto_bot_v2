from aiogram import Router, types, F
from aiogram.filters import Command

from keyboards.start_kb import main

router = Router()


@router.callback_query(F.data == 'start')
@router.message(Command(commands=['start']))
async def start_bot(message: types.Message | types.CallbackQuery):
    answer = f'–ú–µ–Ω—è –ó–æ–≤—É—Ç Avto_bot!ü§ñ \n\n–Ø Telegram-–±–æ—Ç –¥–ª—è —Ä–∞—Å—á–µ—Ç–∞ –ø—Ä–∏–≤–æ–∑–∞ –∫–æ—Ä–µ–π—Å–∫–∏—Ö –∞–≤—Ç–æ–º–æ–±–∏–ª–µ–π, —è –ø–æ–º–æ–≥—É ' \
             f'—Ä–∞—Å—Å—á–∏—Ç–∞—Ç—å —Ü–µ–Ω—É –∏ –¥—Ä—É–≥–∏–µ –¥–µ—Ç–∞–ª–∏ –ø–æ –¥–æ—Å—Ç–∞–≤–∫–µ –∞–≤—Ç–æ–º–æ–±–∏–ª—è.\n\n' \
             f'üí∞ –Ø –ø—Ä–µ–¥–æ—Å—Ç–∞–≤–ª—è—é —Ç–∞–∫—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é, –∫–∞–∫ —Ü–µ–Ω—ã, —Ö–∞—Ä–∞–∫—Ç–µ—Ä–∏—Å—Ç–∏–∫–∏ –∏ —Å–ø–µ—Ü–∏—Ñ–∏–∫–∞—Ü–∏–∏ –¥–ª—è —Ä–∞–∑–ª–∏—á–Ω—ã—Ö ' \
             f'–º–æ–¥–µ–ª–µ–π –∞–≤—Ç–æ–º–æ–±–∏–ª–µ–π –∫–æ—Ä–µ–π—Å–∫–∏—Ö –ø—Ä–æ–∏–∑–≤–æ–¥–∏—Ç–µ–ª–µ–π.'

    if message.__class__ is types.Message:
        await message.answer(f'üñê –ü—Ä–∏–≤–µ—Ç, {message.from_user.username}!\n\n {answer}', reply_markup=main())
    elif message.__class__ is types.CallbackQuery:
        await message.message.answer(f'üñê –ü—Ä–∏–≤–µ—Ç, {message.from_user.username}!\n\n {answer}', reply_markup=main())
